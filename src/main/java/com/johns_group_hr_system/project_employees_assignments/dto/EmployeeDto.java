package com.johns_group_hr_system.project_employees_assignments.dto;

import java.time.LocalDate;
import java.util.List;
import java.util.UUID;

public class EmployeeDto {

    private UUID id;
    private String name;
    private String email;
    private String team;
    private String role;
    private int score;
    private LocalDate birthDate;
    private LocalDate recrutementDate;
    private String gender;
    private int salary;
    private List<ProjectDto> projects;

    public UUID getId() {
        return id;
    }

    public void setId(UUID id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getTeam() {
        return team;
    }

    public void setTeam(String team) {
        this.team = team;
    }

    public String getRole() {
        return role;
    }

    public void setRole(String role) {
        this.role = role;
    }

    public int getScore() {
        return score;
    }

    public void setScore(int score) {
        this.score = score;
    }

    public LocalDate getBirthDate() {
        return birthDate;
    }

    public void setBirthDate(LocalDate birthDate) {
        this.birthDate = birthDate;
    }

    public LocalDate getRecrutementDate() {
        return recrutementDate;
    }

    public void setRecrutementDate(LocalDate recrutementDate) {
        this.recrutementDate = recrutementDate;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public int getSalary() {
        return salary;
    }

    public void setSalary(int salary) {
        this.salary = salary;
    }

    public List<ProjectDto> getProjects() {
        return projects;
    }

    public void setProjects(List<ProjectDto> projects) {
        this.projects = projects;
    }
}